package com.mysoft.app.stack;

/**
 * ==========================================
 * 描述：栈操作的接口
 * ------------------------------------------
 * @author lnsoftware
 * ------------------------------------------
 * @date 2015年11月7日
 * ==========================================
 */
public interface MyStack<T> {

	/**
	 * 判断栈是否为空
	 */
	boolean isEmpty();
	
	/**
	 * 栈的长度
	 */
	int length();
	
	/**
	 * 返回栈顶元素
	 */
	T top();
	
	/**
	 * 数据入栈
	 */
	boolean push(T data);
	
	/**
	 * 数据出栈
	 */
	T pop();
}
